static int8_t zf_attestation_consistency_cache = -1;

static bool zf_attestation_contains(const uint8_t *haystack, size_t haystack_len,
                                    const uint8_t *needle, size_t needle_len) {
    if (!haystack || !needle || needle_len == 0 || haystack_len < needle_len) {
        return false;
    }

    for (size_t i = 0; i <= haystack_len - needle_len; ++i) {
        if (memcmp(&haystack[i], needle, needle_len) == 0) {
            return true;
        }
    }

    return false;
}

static bool zf_attestation_parse_der_length(const uint8_t *input, size_t input_len,
                                            size_t *header_len, size_t *value_len) {
    if (!input || input_len < 2 || !header_len || !value_len) {
        return false;
    }

    if ((input[1] & 0x80U) == 0) {
        *header_len = 2;
        *value_len = input[1];
        return *value_len <= input_len - *header_len;
    }

    size_t length_octets = input[1] & 0x7FU;
    if (length_octets == 0 || length_octets > sizeof(size_t) || length_octets > input_len - 2U) {
        return false;
    }

    size_t length = 0;
    for (size_t i = 0; i < length_octets; ++i) {
        length = (length << 8) | input[2 + i];
    }

    *header_len = 2 + length_octets;
    *value_len = length;
    return *value_len <= input_len - *header_len;
}

static bool zf_attestation_extract_leaf_signature(const uint8_t **out_tbs, size_t *out_tbs_len,
                                                  const uint8_t **out_signature,
                                                  size_t *out_signature_len) {
    size_t outer_header_len = 0;
    size_t outer_value_len = 0;
    size_t tbs_header_len = 0;
    size_t tbs_value_len = 0;
    size_t algorithm_header_len = 0;
    size_t algorithm_value_len = 0;
    size_t signature_header_len = 0;
    size_t signature_value_len = 0;
    size_t offset = 0;

    if (!out_tbs || !out_tbs_len || !out_signature || !out_signature_len) {
        return false;
    }
    if (!zf_attestation_parse_der_length(zf_attestation_cert, sizeof(zf_attestation_cert),
                                         &outer_header_len, &outer_value_len)) {
        return false;
    }
    if (outer_value_len != sizeof(zf_attestation_cert) - outer_header_len) {
        return false;
    }

    offset = outer_header_len;
    if (offset >= sizeof(zf_attestation_cert) || zf_attestation_cert[offset] != 0x30) {
        return false;
    }
    if (!zf_attestation_parse_der_length(&zf_attestation_cert[offset],
                                         sizeof(zf_attestation_cert) - offset, &tbs_header_len,
                                         &tbs_value_len)) {
        return false;
    }
    if (tbs_header_len > sizeof(zf_attestation_cert) - offset ||
        tbs_value_len > sizeof(zf_attestation_cert) - offset - tbs_header_len) {
        return false;
    }
    *out_tbs = &zf_attestation_cert[offset];
    *out_tbs_len = tbs_header_len + tbs_value_len;

    offset += *out_tbs_len;
    if (offset >= sizeof(zf_attestation_cert) || zf_attestation_cert[offset] != 0x30) {
        return false;
    }
    if (!zf_attestation_parse_der_length(&zf_attestation_cert[offset],
                                         sizeof(zf_attestation_cert) - offset,
                                         &algorithm_header_len, &algorithm_value_len)) {
        return false;
    }

    if (algorithm_header_len > sizeof(zf_attestation_cert) - offset ||
        algorithm_value_len > sizeof(zf_attestation_cert) - offset - algorithm_header_len) {
        return false;
    }
    offset += algorithm_header_len + algorithm_value_len;
    if (offset >= sizeof(zf_attestation_cert) || zf_attestation_cert[offset] != 0x03) {
        return false;
    }
    if (!zf_attestation_parse_der_length(&zf_attestation_cert[offset],
                                         sizeof(zf_attestation_cert) - offset,
                                         &signature_header_len, &signature_value_len)) {
        return false;
    }
    if (signature_header_len > sizeof(zf_attestation_cert) - offset ||
        signature_value_len > sizeof(zf_attestation_cert) - offset - signature_header_len ||
        signature_value_len < 1 || zf_attestation_cert[offset + signature_header_len] != 0x00) {
        return false;
    }

    *out_signature = &zf_attestation_cert[offset + signature_header_len + 1];
    *out_signature_len = signature_value_len - 1;
    return true;
}

const uint8_t *zf_attestation_get_aaguid(void) {
    return zf_attestation_aaguid;
}

const char *zf_attestation_get_aaguid_string(void) {
    return zf_attestation_aaguid_string;
}

const uint8_t *zf_attestation_get_leaf_cert_der(size_t *out_len) {
    *out_len = sizeof(zf_attestation_cert);
    return zf_attestation_cert;
}

const uint8_t *zf_attestation_get_leaf_private_key(void) {
    return zf_attestation_private_key;
}

size_t zf_attestation_get_cert_chain(const uint8_t **certs, size_t *cert_lens, size_t max_certs) {
    if (!certs || !cert_lens || max_certs == 0) {
        return 0;
    }

    certs[0] = zf_attestation_cert;
    cert_lens[0] = sizeof(zf_attestation_cert);
    return 1;
}

bool zf_attestation_sign_input(const uint8_t *input, size_t input_len, uint8_t *out,
                               size_t out_capacity, size_t *out_len) {
    uint8_t hash[32];

    if (!input || input_len == 0) {
        return false;
    }

    zf_crypto_sha256(input, input_len, hash);
    return zf_crypto_sign_hash_with_private_key(zf_attestation_private_key, hash, out, out_capacity,
                                                out_len);
}

bool zf_attestation_validate_consistency(void) {
    if (zf_attestation_consistency_cache >= 0) {
        return zf_attestation_consistency_cache != 0;
    }

    const uint8_t *leaf_tbs = NULL;
    const uint8_t *leaf_signature = NULL;
    size_t leaf_tbs_len = 0;
    size_t leaf_signature_len = 0;
    uint8_t leaf_tbs_hash[32];
    uint8_t public_x[ZF_PUBLIC_KEY_LEN];
    uint8_t public_y[ZF_PUBLIC_KEY_LEN];

    // Validate AAGUID and certificate structure
    bool ok = sizeof(zf_attestation_aaguid) == ZF_AAGUID_LEN;
    ok = ok && zf_attestation_contains(zf_attestation_cert, sizeof(zf_attestation_cert),
                                       zf_attestation_aaguid_extension,
                                       sizeof(zf_attestation_aaguid_extension));
    ok = ok && zf_attestation_contains(zf_attestation_cert, sizeof(zf_attestation_cert),
                                       zf_attestation_aaguid, sizeof(zf_attestation_aaguid));

    // Validate leaf certificate public key
    ok = ok &&
         zf_crypto_compute_public_key_from_private(zf_attestation_private_key, public_x, public_y);
    ok = ok && memcmp(public_x, zf_attestation_leaf_public_x, sizeof(public_x)) == 0 &&
         memcmp(public_y, zf_attestation_leaf_public_y, sizeof(public_y)) == 0;

    if (ok) {
        uint8_t leaf_public_key_uncompressed[1 + ZF_PUBLIC_KEY_LEN * 2];
        zf_attestation_build_uncompressed_public_key(public_x, public_y,
                                                     leaf_public_key_uncompressed);
        ok = zf_attestation_contains(zf_attestation_cert, sizeof(zf_attestation_cert),
                                     leaf_public_key_uncompressed,
                                     sizeof(leaf_public_key_uncompressed));
    }

    // Validate root certificate
    ok = ok && zf_attestation_contains(zf_attestation_root_cert, sizeof(zf_attestation_root_cert),
                                       zf_attestation_root_key_identifier,
                                       sizeof(zf_attestation_root_key_identifier));

    ok = ok && zf_attestation_contains(zf_attestation_root_cert, sizeof(zf_attestation_root_cert),
                                       zf_attestation_root_public_key_uncompressed,
                                       sizeof(zf_attestation_root_public_key_uncompressed));

    // Validate certificate chain
    ok = ok && zf_attestation_contains(zf_attestation_cert, sizeof(zf_attestation_cert),
                                       zf_attestation_root_key_identifier,
                                       sizeof(zf_attestation_root_key_identifier));

    // Verify leaf certificate signature
    ok = ok && zf_attestation_extract_leaf_signature(&leaf_tbs, &leaf_tbs_len, &leaf_signature,
                                                     &leaf_signature_len);
    if (ok) {
        zf_crypto_sha256(leaf_tbs, leaf_tbs_len, leaf_tbs_hash);
        ok = zf_crypto_verify_hash_with_public_key(zf_attestation_root_public_x,
                                                   zf_attestation_root_public_y, leaf_tbs_hash,
                                                   leaf_signature, leaf_signature_len);
    }

    zf_attestation_consistency_cache = ok ? 1 : 0;

    return ok;
}

void zf_attestation_reset_consistency_cache(void) {
    zf_attestation_consistency_cache = -1;
}
